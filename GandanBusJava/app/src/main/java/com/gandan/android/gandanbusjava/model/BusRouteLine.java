package com.gandan.android.gandanbusjava.model;

public class BusRouteLine {

    private String routeId;
    private String locationSeq;
    private String locationTp;
    private String locationId;
    private String subLength;
    private String sumLength;
    private String x;
    private String y;
    private String linkId;

    public String getRouteId() {
        return routeId;
    }

    public void setRouteId(String routeId) {
        this.routeId = routeId;
    }

    public String getLocationSeq() {
        return locationSeq;
    }

    public void setLocationSeq(String locationSeq) {
        this.locationSeq = locationSeq;
    }

    public String getLocationTp() {
        return locationTp;
    }

    public void setLocationTp(String locationTp) {
        this.locationTp = locationTp;
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(String locationId) {
        this.locationId = locationId;
    }

    public String getSubLength() {
        return subLength;
    }

    public void setSubLength(String subLength) {
        this.subLength = subLength;
    }

    public String getSumLength() {
        return sumLength;
    }

    public void setSumLength(String sumLength) {
        this.sumLength = sumLength;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getLinkId() {
        return linkId;
    }

    public void setLinkId(String linkId) {
        this.linkId = linkId;
    }
}
